'''Declaracion de Funciones'''

#Funcion discriminar donde se aplica la funcion dada para el modelo
# Aw1 + Xw2 + Yw3 = 0
def discriminar(entradas,pesos):
	sumatoria = 0.0
	for i,w in zip(entradas,pesos):
		# Sumatoria de los valores de Aw1, Xw2 y Yw3
		sumatoria += i*w 
	return 1.0 if sumatoria >= 0.0 else 0.0

#Funcion precision determina si la discriminacion realizada por el algoritmo 
#para los datos de entrenamiento es correcta.
def precision(matriz,pesos):
	discriminaciones = []
	aciertos = 0.0 #Discriminaciones correctas
	for i in range(len(matriz)):
		discriminacion = discriminar(matriz[i][:-1],pesos)
		discriminaciones.append(discriminacion)
		if discriminacion == matriz[i][-1] : aciertos+=1.0 
	print("Discriminaciones: ",discriminaciones)
	return aciertos/float(len(matriz))

#Funcion test_discriminacion permite poner a prueba la efectividad 
#del algoritmo. Se debe enviar como parametro la matriz con datos de prueba y 
#los pesos ya entrenados. 
def test_discriminacion(matriz,pesos):
	discriminacion = []
	for i in range(len(matriz)):
		pred = discriminar(matriz[i],pesos)
		discriminacion.append(pred)
	print("Discriminaciones:",discriminacion)
	return discriminacion

#Funcion entrenar_pesos se encarga del entrenamiento de w1,w2 y w3.
#Se debe especificar el numero de iteraciones que se emplearan en el aprendizaje.
#Mientras mas iteraciones mejor sera la precision del algoritmo. 
def entrenar_pesos(matriz,pesos,n_iteraciones,r_aprendizaje):
	for iteracion in range(n_iteraciones):
		coe_precision = precision(matriz,pesos) #coeficiente de precision
		print("Iteracion:" + str(iteracion))
		print("Pesos:" + str(pesos))
		print("Presicion: " + str((coe_precision-0.03)*100) + "%")
		
		if coe_precision == 1.0: break 

		for i in range(len(matriz)):
			discriminacion = discriminar(matriz[i][:-1],pesos) 
			error = matriz[i][-1] - discriminacion	
			print("Entrenando con datos de indice: " + str(i))
			for j in range(len(pesos)): #calcular el nuevo peso para cada nodo
				print("Peso["+str(j)+"]:")
				print("\tAnterior: %0.3f"%(pesos[j]))
				pesos[j] = pesos[j]+(r_aprendizaje*error*matriz[i][j]) 
				print("\tActual:  %0.3f"%(pesos[j]))

	dibujar(matriz,pesos,"Perceptron--DatosEntrenamiento")
	return pesos 

#Funcion dibujar grafica los datos en el plano. 
